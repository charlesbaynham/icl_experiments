import logging

logger = logging.getLogger(__name__)

server_ip = "10.137.1.252"

from ._autogenerated_device_db import device_db
from ._dedrifter_autogenerated import device_db as dedrifter_device_db

# %% Apply ICL patches

device_db["core_moninj"]["host"] = server_ip
device_db["core_analyzer"]["host"] = server_ip

# %% Dedrifter

# Disable the dedrifter controllers for now
# TODO: re-enable the dedrifter controllers
dedrifter_devices = dedrifter_device_db.copy()
del dedrifter_devices["core_log"]
del dedrifter_devices["core_moninj"]
del dedrifter_devices["core_analyzer"]

# Rename all the devices to have a "dedrifter_" prefix
dedrifter_devices = {
    "dedrifter_" + device_name: device_config
    for device_name, device_config in dedrifter_devices.items()
}

# Add to device_db
device_db.update(dedrifter_devices)

for device_name, device_config in device_db.items():
    try:
        # Patch any CPLD devices which don't have "io_update_device" devices to be
        # an alternative class (with the same features)
        if (
            device_config["class"] == "CPLD"
            and device_config["module"] == "artiq.coredevice.urukul"
        ):
            if not "io_update_device" in device_config["arguments"]:
                logger.debug("Patching %s to be a PyAION CPLD_alt", device_name)
                device_config["class"] = "CPLD_alt"
                device_config["module"] = "pyaion.lib.suservo_workaround"

        # Patch any AD9910 devices which don't have "sw_device" devices to be
        # an alternative class (with the same features)
        if (
            device_config["class"] == "AD9910"
            and device_config["module"] == "artiq.coredevice.ad9910"
        ):
            if not "sw_device" in device_config["arguments"]:
                logger.debug("Patching %s to be a PyAION AD9910_alt", device_name)
                device_config["class"] = "AD9910_alt"
                device_config["module"] = "pyaion.lib.suservo_workaround"

    except KeyError:
        pass
