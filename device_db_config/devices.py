import importlib
import logging

from . import _aliases as aliases
from . import _autogenerated_device_db as generated_device_db
from . import _non_core_devices as non_core_devices
from . import configuration as config

logger = logging.getLogger(__name__)
if logger.level <= logging.INFO:
    import pprint


server_ip = "10.137.1.252"


def get_device_db(simulation_mode=False):
    """
    Returns the device_db, including both hardware identifiers and aliases.

    This function always reloads the libraries even if they have already been imported.
    """

    # Force reload of the modules in case they have been updated
    importlib.reload(aliases)
    importlib.reload(generated_device_db)
    importlib.reload(non_core_devices)
    importlib.reload(generated_device_db)

    db = generated_device_db.device_db.copy()

    # Append our own peripheral devices
    db = _append_non_core(db, simulation_mode)

    # Append our own aliases, describing the purposes of the channels
    db = _append_aliases(db)

    # Apply ICL patches to the device db
    db = apply_icl_patches(db)

    logger.debug("DeviceDB import performed, resulting in device_db:")
    logger.debug(pprint.pformat(db))

    return db


def get_configuration_from_db(key):
    """
    Get the config item saved in module :mod:`.configuration` under `key`. This
    can be any datatype.
    """
    configuration_db = config.config

    item = configuration_db[key]

    return item


def apply_icl_patches(device_db):
    """
    Apply ICL patches

    Patch the device db to:
    * Avoid the ARTIQ bug of objects with differing attributes
    * Specify the binding IP addresses for controllers
    """

    device_db = device_db.copy()  # Avoid modifying the original db

    for device_name, device_config in device_db.items():
        # Skip aliases
        if not isinstance(device_config, dict):
            continue

        # If this is a controller and the "host" is "::1", change it to the server IP & remove the localhost binding.
        if (
            "type" in device_config
            and "host" in device_config
            and "command" in device_config
            and device_config["type"] == "controller"
            and device_config["host"] == "::1"
        ):
            logger.debug("Patching %s to use server IP %s", device_name, server_ip)
            device_config["host"] = server_ip
            device_config["command"] = device_config["command"] + " --no-localhost-bind"

        try:
            # Patch any CPLD devices which don't have "io_update_device" devices to be
            # an alternative class (with the same features)
            if (
                device_config["class"] == "CPLD"
                and device_config["module"] == "artiq.coredevice.urukul"
            ):
                if not "io_update_device" in device_config["arguments"]:
                    logger.debug("Patching %s to be a PyAION CPLD_alt", device_name)
                    device_config["class"] = "CPLD_alt"
                    device_config["module"] = "pyaion.lib.suservo_workaround"

            # Patch any AD9910 devices which don't have "sw_device" devices to be
            # an alternative class (with the same features)
            if (
                device_config["class"] == "AD9910"
                and device_config["module"] == "artiq.coredevice.ad9910"
            ):
                if not "sw_device" in device_config["arguments"]:
                    logger.debug("Patching %s to be a PyAION AD9910_alt", device_name)
                    device_config["class"] = "AD9910_alt"
                    device_config["module"] = "pyaion.lib.suservo_workaround"

        except KeyError:
            pass

    return device_db


def get_all_configurations_from_db():
    """
    Get all configuration entries saved in module :mod:`.configuration`.
    """
    return config.config


def _append_aliases(db: dict):
    # Merge dicts
    return {**db, **aliases.aliases}


def _append_non_core(db: dict, simulation_mode=False):
    non_core = non_core_devices.get_non_core_devices(simulation_mode)

    # Merge dicts
    return {**db, **non_core}
