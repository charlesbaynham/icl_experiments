def estimate_initial_params_fft(x, y, data):
    """
    Generated by chatgpt
    Estimate initial parameters for a 2D Gaussian using an FFT-based approach.

    The function combines spatial and frequency-domain warningrmation:
      1. Computes the offset as the minimum value of `data`.
      2. Computes the amplitude as max(data) minus the offset.
      3. Estimates the center (x₀, y₀) using the spatial center of mass.
      4. Computes the 2D FFT (with offset subtracted) and then the log-transformed
         FFT magnitude.
      5. Constructs a radial profile of the FFT log magnitude by binning in the frequency domain.
      6. Finds the frequency at which the radial profile reaches half its maximum value,
         and estimates σ using the relation: σ = 1/(2π·f_half).
      7. Estimates the orientation θ from the weighted covariance of the FFT frequency components.

    Parameters
    ----------
    x : ndarray
        2D grid of x coordinates.
    y : ndarray
        2D grid of y coordinates.
    data : ndarray
        2D array containing the Gaussian data.

    Returns
    -------
    tuple
        Initial estimates for (amplitude, x₀, y₀, σₓ, σᵧ, θ, offset).
    """
    # Estimate offset and amplitude
    offset = mean_edge_values(data)
    amplitude = np.max(data) - offset

    # Estimate center from spatial center of mass
    y0, x0 = center_of_mass(data)

    # Compute the FFT of (data - offset) and shift zero-frequency to center
    fft_data = fftshift(fft2(data - offset))
    fft_mag = np.abs(fft_data)
    fft_log = np.log1p(fft_mag)  # log transform to suppress large dynamic range

    # Build normalized frequency grids based on data shape
    shape = data.shape
    fy = np.fft.fftfreq(shape[0])
    fx = np.fft.fftfreq(shape[1])
    fx = fftshift(fx)
    fy = fftshift(fy)
    fx_grid, fy_grid = np.meshgrid(fx, fy)
    f_r = np.sqrt(fx_grid**2 + fy_grid**2)

    # Create a radial profile: bin the log FFT magnitude as a function of frequency radius.
    nbins = 50
    f_r_flat = f_r.ravel()
    fft_log_flat = fft_log.ravel()
    bins = np.linspace(f_r_flat.min(), f_r_flat.max(), nbins + 1)
    bin_centers = 0.5 * (bins[:-1] + bins[1:])
    radial_mean = np.zeros(nbins)
    for i in range(nbins):
        mask = (f_r_flat >= bins[i]) & (f_r_flat < bins[i + 1])
        if np.any(mask):
            radial_mean[i] = np.mean(fft_log_flat[mask])
        else:
            radial_mean[i] = 0

    # Determine the frequency at half maximum
    half_max = np.max(radial_mean) / 2
    indices = np.where(radial_mean >= half_max)[0]
    if indices.size > 0:
        f_half = bin_centers[indices[-1]]
    else:
        f_half = bin_centers[0]

    # Use the Fourier relation to estimate sigma:
    sigma_est = 1.0 / (2 * np.pi * f_half) if f_half > 0 else len(data) / 4
    sigma_x = sigma_y = 2 * sigma_est

    # Estimate orientation from the frequency domain: compute the weighted covariance
    weight = fft_mag.ravel()
    mean_fx = np.sum(fx_grid.ravel() * weight) / np.sum(weight)
    mean_fy = np.sum(fy_grid.ravel() * weight) / np.sum(weight)
    cov_xx = np.sum(weight * (fx_grid.ravel() - mean_fx) ** 2) / np.sum(weight)
    cov_yy = np.sum(weight * (fy_grid.ravel() - mean_fy) ** 2) / np.sum(weight)
    cov_xy = np.sum(
        weight * (fx_grid.ravel() - mean_fx) * (fy_grid.ravel() - mean_fy)
    ) / np.sum(weight)
    theta = 0.5 * np.arctan2(2 * cov_xy, cov_xx - cov_yy)

    return amplitude, x0, y0, sigma_x, sigma_y, theta, offset


def mean_edge_values(arr: np.ndarray):
    """Computes the mean of all edge values in a 2D NumPy array."""
    top = arr[0, :]  # Top row
    bottom = arr[-1, :]  # Bottom row
    left = arr[:, 0]  # Left column
    right = arr[:, -1]  # Right column

    # Combine all edges without double-counting corners
    edge_values = np.concatenate([top, bottom, left[1:-1], right[1:-1]])

    return np.mean(edge_values)


# Pre-processing: Estimate initial parameters
def estimate_initial_params_com(x: np.ndarray, y: np.ndarray, data: np.ndarray):
    """
    Generated by chatgpt
    """
    offset = np.min(data)
    amplitude = np.max(data) - offset
    y0, x0 = np.unravel_index(np.argmax(data), data.shape)  # Peak position

    # Estimate sigmas using second moments
    mass = data - offset
    x_mean, y_mean = center_of_mass(mass)
    x_var = np.sum((x - x_mean) ** 2 * mass) / np.sum(mass)
    y_var = np.sum((y - y_mean) ** 2 * mass) / np.sum(mass)
    sigma_x, sigma_y = np.sqrt(x_var) / 2, np.sqrt(y_var) / 2

    # Estimate orientation (theta) using SVD
    x_flat, y_flat = x.ravel(), y.ravel()
    coords = np.vstack((x_flat - x0, y_flat - y0))
    U, S, Vt = np.linalg.svd(coords @ coords.T)
    theta = np.arctan2(Vt[0, 1], Vt[0, 0])  # Principal axis angle

    return amplitude, x_mean, y_mean, sigma_x, sigma_y, theta, offset


# Generate synthetic 2D Gaussian data
def generate_data(size=50, noise=0.1):
    x = np.linspace(0, size, size)
    y = np.linspace(0, size, size)
    x, y = np.meshgrid(x, y)

    true_params = (
        3,
        25,
        25,
        5,
        8,
        np.pi / 4,
        10,
    )  # (amplitude, x0, y0, sigma_x, sigma_y, theta, offset)
    data = gaussian_2d((x, y), *true_params)
    noise = np.random.normal(0, noise, data.shape)
    return x, y, data + noise, true_params

def get_bounds_1d(x: np.ndarray, data: np.ndarray):
    # amplitude, xo, sigma, offset
    bounds_A = (-np.inf, np.inf)
    bounds_x0 = (x.min(), x.max())
    bounds_sigma = (0, x.max() - x.min())
    bounds_offset = (-np.inf, np.inf)
    bounds = list(zip(*[bounds_A, bounds_x0, bounds_sigma, bounds_offset]))
    return bounds


def get_bounds_2d(x: np.ndarray, y: np.ndarray, data: np.ndarray):
    # amplitude, x0, y0, sigma_x, sigma_y, theta, offset
    bounds_A = (np.min(data), 1.5 * np.max(data))
    bounds_x0 = (x.min(), x.max())
    bounds_y0 = (y.min(), y.max())
    bounds_sigma_x = (0, x.max() - x.min())
    bounds_sigma_y = (0, y.max() - y.min())
    bounds_theta = (0, np.pi / 2)
    bounds_offset = (np.min(data), 0.5 * np.max(data))
    bounds = list(
        zip(
            *[
                bounds_A,
                bounds_x0,
                bounds_y0,
                bounds_sigma_x,
                bounds_sigma_y,
                bounds_theta,
                bounds_offset,
            ]
        )
    )
    return bounds


def fit_gaussian(
    data: np.ndarray, xy: Optional[tuple]=None, estimator="1d", fitter="curve_fit", print_time=False, method="trf"
):
    if xy is None:
        x, y = np.meshgrid(np.arange(data.shape[1]), np.arange(data.shape[0]))
    else:
        x, y = xy
    bounds = get_bounds_2d(x, y, data)
    xdata = np.vstack((x.ravel(), y.ravel()))
    # t0 = time.time()
    if estimator == "fft":
        initial_guess = estimate_initial_params_fft(x, y, data)
    elif estimator == "com":
        initial_guess = estimate_initial_params_com(x, y, data)
    elif estimator == "1d":
        initial_guess = estimate_initial_params_1d_fit(x, y, data)
    elif estimator == "manual":
        initial_guess = estimate_inital_params(x, y, data)
    else:
        raise ValueError("Invalid estimator")
    # print("Time to estimate initial parameters:", time.time() - t0)
    # t0 = time.time()
    # logger.warning("Initial guess:")
    # logger.warning("A: %f, x0: %f, y0: %f, sigma_x: %f, sigma_y: %f", *initial_guess[:5])
    # logger.warning("theta: %f, offset: %f", initial_guess[5], initial_guess[6])
    if method == "lm":
        kwargs = {"method": method}
    else:
        kwargs = {"method": method, "bounds": bounds}
    if fitter == "curve_fit":
        popt, _ = opt.curve_fit(
            gaussian_2d, xdata, data.ravel(), p0=initial_guess, **kwargs
        )
    elif fitter == "leastsq":
        res = least_squares(
            residuals, initial_guess, args=(xdata, data.ravel()), **kwargs
        )
        popt = res.x
    else:
        raise ValueError("Invalid fitter")

    # logger.warning("Fit params:")
    # logger.warning(
    #     "A: %f, x0: %f, y0: %f, sigma_x: %f, sigma_y: %f, theta: %f, offset: %f",
    #     *popt
    # )
    # print("Time to fit:", time.time() - t0)
    return popt, initial_guess


