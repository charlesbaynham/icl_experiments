def gaussian(x, A, x0, sigma, c):
    return A * np.exp(-((x - x0) ** 2) / (2 * sigma**2)) + c


def gaussian_2d(pos, amplitude, x0, y0, sigma_x, sigma_y, theta, offset):
    """
    Generated by chatgpt
    Generates a 2D Gaussian function.

    Parameters:
    -----------
    pos: tuple(float, int, ndarray)
        (x,y) position(s) at which to evaluate the Gaussian.
    amplitude : float
        Peak amplitude of the Gaussian.
    xo : float
        X-coordinate of the Gaussian center.
    yo : float
        Y-coordinate of the Gaussian center.
    sigma_x : float
        Standard deviation along the x-axis.
    sigma_y : float
        Standard deviation along the y-axis.
    theta : float
        Rotation angle (in radians) of the Gaussian.
    offset : float
        Constant offset added to the Gaussian.

    Returns:
    --------
    ndarray
        2D array representing the Gaussian function evaluated at (x, y).
    """

    x, y = pos[0], pos[1]
    a = (np.cos(theta) ** 2) / (2 * sigma_x**2) + (np.sin(theta) ** 2) / (
        2 * sigma_y**2
    )
    b = -(np.sin(2 * theta)) / (4 * sigma_x**2) + (np.sin(2 * theta)) / (4 * sigma_y**2)
    c = (np.sin(theta) ** 2) / (2 * sigma_x**2) + (np.cos(theta) ** 2) / (
        2 * sigma_y**2
    )

    return offset + amplitude * np.exp(
        -(a * (x - x0) ** 2 + 2 * b * (x - x0) * (y - y0) + c * (y - y0) ** 2)
    )


# Residual function for least squares
def residuals(params, p, data):
    return (gaussian_2d(p, *params) - data).ravel()


