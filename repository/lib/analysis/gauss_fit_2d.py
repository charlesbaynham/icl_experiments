def rotate_image(image, X, Y, angle):
    """
    Rotates a 2D image by an arbitrary angle while preserving the spatial grid.

    Parameters:
    - image: 2D numpy array representing the image.
    - X, Y: Meshgrid coordinate arrays (same shape as image).
    - angle: Rotation angle in degrees (counterclockwise).

    Returns:
    - rotated_image: 2D numpy array of the rotated image.
    - X_rot, Y_rot: Meshgrid coordinates after rotation.
    """
    angle_rad = np.deg2rad(angle)

    # Compute the center of rotation (image center)
    x0, y0 = np.mean(X), np.mean(Y)

    # Compute rotation matrix
    cos_a, sin_a = np.cos(angle_rad), np.sin(angle_rad)
    R = np.array([[cos_a, -sin_a], [sin_a, cos_a]])

    # Flatten the meshgrid
    X_flat, Y_flat = X.ravel(), Y.ravel()

    # Shift to origin, apply rotation, shift back
    X_shifted, Y_shifted = X_flat - x0, Y_flat - y0
    X_rot_flat, Y_rot_flat = R @ np.vstack([X_shifted, Y_shifted])
    X_rot_flat, Y_rot_flat = X_rot_flat + x0, Y_rot_flat + y0

    # Reshape back to image shape
    X_rot, Y_rot = X_rot_flat.reshape(X.shape), Y_rot_flat.reshape(Y.shape)

    # Interpolate the image values at the new rotated coordinates
    rotated_image = ndi.map_coordinates(
        image, [Y_rot_flat, X_rot_flat], order=1, mode="nearest"
    )
    rotated_image = rotated_image.reshape(image.shape)

    return rotated_image, X_rot, Y_rot


