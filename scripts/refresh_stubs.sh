#!/usr/bin/env bash
#
# Build (or refresh) the stub catalog that the ARTIQ master serves as its
# experiment repository, and print the path of the resulting worktree.
#
# The catalog lives in a *linked git worktree* under the user's cache dir,
# parked on a local, unrelated-histories "stubs" branch that is regenerated on
# every run and never pushed. Because the worktree shares the main repo's object
# database and refs, submitting an experiment by revision (e.g. repo_rev=master)
# still resolves the real code, while the worktree's own HEAD carries the stub
# catalog. The real device_db is unaffected: the master loads it from its own
# working directory, not from this worktree.
#
# This operation is create-or-refresh idempotent: first boot, a reboot, or a
# wiped cache all behave the same. Run from the repo root (the dir holding
# .aion_artiq_root). Progress goes to stderr; the ONLY stdout line is the
# worktree path, so callers can do:  WT="$(scripts/refresh_stubs.sh)"
#
# Used by: the full_stack launch wrapper (builds the worktree before the master
# starts) and `nix run .#refresh_stubs` (rebuild the catalog on demand). To make
# a running master pick up the change, follow it with:
#   artiq_client scan-repository
set -euo pipefail

log() { printf '  [refresh_stubs] %s\n' "$*" >&2; }

BRANCH=stubs
STUBS_WT="${STUBS_WT:-${XDG_CACHE_HOME:-$HOME/.cache}/icl_experiments/stubs}"
CONFIG="${STUBS_CONFIG:-stubs_sources.yaml}"
GENERATOR="scripts/generate_stubs.py"

if [[ ! -f "$CONFIG" || ! -f "$GENERATOR" ]]; then
    log "must be run from the repository root (missing $CONFIG or $GENERATOR)"
    exit 1
fi

# Best-effort fetch so origin/* refs listed in the config resolve. Never fatal:
# a purely-local branch list works offline.
log "fetching refs (best effort)"
git fetch --all --quiet 2>/dev/null || log "fetch skipped (offline?)"

# Ensure the worktree exists and is checked out on $BRANCH.
git worktree prune 2>/dev/null || true
if ! git -C "$STUBS_WT" rev-parse --is-inside-work-tree >/dev/null 2>&1; then
    log "creating stub worktree at $STUBS_WT"
    mkdir -p "$(dirname "$STUBS_WT")"
    rm -rf "$STUBS_WT"
    if git show-ref --verify --quiet "refs/heads/$BRANCH"; then
        # Branch survived a wiped cache; re-attach a worktree to it.
        git worktree add --force "$STUBS_WT" "$BRANCH" >&2
    else
        # Fresh, parentless (unrelated-histories) stub branch.
        git worktree add --force --no-checkout --detach "$STUBS_WT" >&2
        git -C "$STUBS_WT" checkout --orphan "$BRANCH" >&2
        git -C "$STUBS_WT" rm -rf --cached -q . >/dev/null 2>&1 || true
    fi
else
    log "reusing stub worktree at $STUBS_WT"
fi

# Regenerate from a pristine state: drop everything the generator manages (plus
# any leftovers) except the git admin file, then rebuild the stub tree.
find "$STUBS_WT" -mindepth 1 -maxdepth 1 ! -name .git -exec rm -rf {} +
log "generating stubs from $CONFIG"
# generate_stubs.py takes --branches (stdlib only); read the branch list out of
# the YAML config here, where PyYAML is on the launcher's PATH.
mapfile -t BRANCHES < <(python3 -c '
import sys, yaml
data = yaml.safe_load(open(sys.argv[1])) or {}
branches = data.get("branches")
if not isinstance(branches, list) or not branches or not all(
    isinstance(b, str) for b in branches
):
    sys.exit(f"{sys.argv[1]}: expected a non-empty top-level branches: list")
print("\n".join(branches))
' "$CONFIG")
if [[ ${#BRANCHES[@]} -eq 0 ]]; then
    log "no branches parsed from $CONFIG"
    exit 1
fi
python3 "$GENERATOR" --branches "${BRANCHES[@]}" --output-dir "$STUBS_WT" >&2

# Commit as a single unrelated-history commit (amend the previous one if any).
# Local and throwaway: never pushed, not signed, hooks skipped.
git -C "$STUBS_WT" add -A
commit_args=(--no-verify --no-gpg-sign -m "stub catalog (generated by refresh_stubs.sh)")
if git -C "$STUBS_WT" rev-parse --verify -q HEAD >/dev/null 2>&1; then
    git -C "$STUBS_WT" commit --amend "${commit_args[@]}" -q >&2
else
    git -C "$STUBS_WT" commit "${commit_args[@]}" -q >&2
fi
log "stub catalog ready at $(git -C "$STUBS_WT" rev-parse --short HEAD)"

echo "$STUBS_WT"
